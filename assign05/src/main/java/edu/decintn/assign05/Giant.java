package edu.decintn.assign05;

public class Giant {
    public String giantName;

    public Giant(String name)
    {
        giantName = name;
    }

    public String getName()
    {
        return giantName;
    }

    public void setName(String name)
    {
        giantName = name;
    }

    public String toString()
    {
        return giantName;
    }

}
